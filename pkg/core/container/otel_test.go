func TestRenderOTelEnvFile(t *testing.T) {
	env := map[string]string{
		"OTEL_EXPORTER_OTLP_ENDPOINT": "http://10.0.3.5:4318",
		"CONTAINARIUM_CONTAINER_ID":   "alice-container",
		"OTEL_EXPORTER_OTLP_HEADERS":  "Authorization=Bearer abc123",
	}
	got := string(RenderOTelEnvFile(env))

	// Header comment present so a human knows it's generated.
	if !strings.HasPrefix(got, "# Generated by Containarium") {
		t.Errorf("missing generated header:\n%s", got)
	}
	// Each var renders as a single KEY=value line, value verbatim —
	// in particular the bearer header keeps its '=' and space intact.
	for _, want := range []string{
		"\nOTEL_EXPORTER_OTLP_ENDPOINT=http://10.0.3.5:4318\n",
		"\nCONTAINARIUM_CONTAINER_ID=alice-container\n",
		"\nOTEL_EXPORTER_OTLP_HEADERS=Authorization=Bearer abc123\n",
	} {
		if !strings.Contains(got, want) {
			t.Errorf("rendered file missing %q in:\n%s", want, got)
		}
	}
	// Deterministic ordering: keys are sorted, so CONTAINARIUM_ precedes
	// OTEL_EXPORTER_OTLP_ENDPOINT precedes OTEL_EXPORTER_OTLP_HEADERS.
	ci := strings.Index(got, "CONTAINARIUM_CONTAINER_ID=")
	ei := strings.Index(got, "OTEL_EXPORTER_OTLP_ENDPOINT=")
	hi := strings.Index(got, "OTEL_EXPORTER_OTLP_HEADERS=")
	if ci >= ei || ei >= hi {
		t.Errorf("keys not sorted deterministically: ci=%d ei=%d hi=%d", ci, ei, hi)
	}
}

func TestRenderOTelEnvFile_Empty(t *testing.T) {
	// Empty map → only comment lines, no KEY=value var lines.
	got := string(RenderOTelEnvFile(map[string]string{}))
	for _, line := range strings.Split(strings.TrimSpace(got), "\n") {
		if line != "" && !strings.HasPrefix(line, "#") {
			t.Errorf("empty map should render only comments, got var line %q", line)
		}
	}
}

